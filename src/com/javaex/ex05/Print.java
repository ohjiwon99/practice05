package com.javaex.ex05;

public class Print {

	// 필드
	private int val;

	// 생성자
	public void printer(int val) {
		System.out.println(val);
	}

	// 메소드 3개 를 작성하세요

	public void printer(Double val) {
		System.out.println(val);
	}

	public void printer(boolean val) {
		System.out.println(val);
	}

	public void printer(String val) {
		System.out.println(val);
	}

}
